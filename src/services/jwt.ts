import JWT from "jsonwebtoken";
import { User } from "next-auth";

const JWT_SECRET = "Super@1234";

class JWTService {
  public static generateTokenForUser(user: User) {
    const payload = {
      id: user?.id,
      email: user?.email,
    };

    const token = JWT.sign(payload, JWT_SECRET);
    return token;
  }
}

export default JWTService;
